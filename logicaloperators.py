"""
Logical operators (8.1.4 (f))
The following symbols are used for logical operators:
= equal to
<> not equal to
< less than
<= less than or equal to
> greater than
>= greater than or equal to

The result of these operations is always of data type BOOLEAN.
In complex expressions, it is advisable to use parentheses () to make the order of operations clear.

"""
# OUTPUT 5=3
print(5==3) #False
# OUTPUT 5<>3
print(5!=3) #True
# OUTPUT 5<3
print(5<3) #False
# OUTPUT 5>3
print(5<=3) #False
# OUTPUT 5>3
print(5>3) #True
# OUTPUT 5>=3
print(5>=3) #True
